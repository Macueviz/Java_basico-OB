package com.primeraPrueba.interfaces;

public class Empleado {

    // 1. A
    String nombre;
    int edad;
    double salario;
    boolean alta;

    // 2. Cons
    public Empleado(){}

    public Empleado(String nombre, int edad, double salario, boolean alta) {
        this.nombre = nombre;
        this.edad = edad;
        this.salario = salario;
        this.alta = alta;
    }
    // 3. Met

}
